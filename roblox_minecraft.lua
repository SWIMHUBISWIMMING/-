local function getNearestplayers()
    local Target = nil
    local Closest = gamesetting.killaurarange
    for _,v in pairs(plrs:GetPlayers()) do
        if (v.Character and v ~= plr) then
            local Distance = (plr.Character:FindFirstChild("HumanoidRootPart").Position - v.Character:FindFirstChild("HumanoidRootPart").Position).Magnitude
            if (Distance < Closest) then
                Closest = Distance
                Target = v.Character
            end
        end
    end
    return Target
end
